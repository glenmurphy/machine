var codes = {};
for (var i in chars) {
  codes[chars[i]] = i;
}

export function fromCode(code) {
  return chars[code];
}

export function fromChar(char) {
  return codes[char];
}

export function stringToCodes(string) {
  var output = [];
  for (var i = 0; i < string.length; i++) {
    output.push(fromChar(string.charAt(i)));
  }
  return output;
}
